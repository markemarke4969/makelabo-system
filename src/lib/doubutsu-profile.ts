/**
 * 副業マッチング × 動物占いのクロス説明文を生成する。
 * 動物の性格を副業タイプの文脈で再解釈し、補強する形で3段落を返す。
 */
export function buildMatchingCrossReading(
  profile: DoubutsuProfile,
  matchingTypeId: string,
): string[] {
  const mod = MATCHING_MODIFIERS[matchingTypeId] ?? MATCHING_MODIFIERS["steady"];
  return [
    `${profile.character}${mod.bridge}`,
    `${mod.strength}`,
    `${mod.advice}`,
  ];
}
