// 動物占い × 投資スタイルのクロス説明文を生成する。
// 戻り値は3段落の文字列配列（既存の result ページの paragraphs と同じ形式）。
export function buildCrossReading(
  profile: DoubutsuProfile,
  investorType: string,
): string[] {
  const mod = INVESTOR_MODIFIERS[investorType] ?? INVESTOR_MODIFIERS["バランス重視型"];
  return [
    profile.character,
    `${profile.workStyle}そんなあなたが投資家として歩むべき道は、${mod.tradingStyle}。キーワードは「${mod.tone}」です。`,
    `${profile.moneyStyle}${mod.advice}`,
  ];
}

